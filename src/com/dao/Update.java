    public void addToDD() {
        try {
            GenericXmlApplicationContext ctx = new GenericXmlApplicationContext();
            ctx.load("app-spring.xml");
            ctx.refresh();
            ContactDao cd = ctx.getBean("contactDao", ContactDao.class);
            for (int i : cd.allId()) {
                jComboBox1.addItem(i);
            }
        } catch (Exception e) {
        }
    }
