    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        GenericXmlApplicationContext ctx = new GenericXmlApplicationContext();
        ctx.load("classpath:app-spring.xml");
        ctx.refresh();
        ContactDao cd = ctx.getBean("contactDao", ContactDao.class);
        Contact con = new Contact();
        con.setId(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
        con.setName(jTextField2.getText());
        con.setPassword(jTextField3.getText());
        con.setBirthdate(new java.util.Date(Date.valueOf(jTextField4.getText()).getTime()));
        cd.doUp(con);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        GenericXmlApplicationContext ctx = new GenericXmlApplicationContext();
        ctx.load("classpath:app-spring.xml");
        ctx.refresh();
        ContactDao cd = ctx.getBean("contactDao", ContactDao.class);
        List <Contact> list=cd.allContactById(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
        Contact ct = list.get(0);
        jTextField2.setText(ct.getName());
        jTextField3.setText(ct.getPassword());
        jTextField4.setText(String.valueOf(ct.getBirthdate()));
    }//GEN-LAST:event_jComboBox1ItemStateChanged

